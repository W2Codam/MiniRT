#include "libft.h"

int32_t	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (((uint8_t *)s1)[i] == ((uint8_t *)s2)[i] && i < n - 1)
		i++;
	return (((uint8_t *)s1)[i] - ((uint8_t *)s2)[i]);
}
