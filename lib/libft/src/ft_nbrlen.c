#include "libft.h"

int	ft_nbrlen(long long nbr)
{
	int	i;

	i = 0;
	if (nbr == 0)
	{
		i++;
		return (i);
	}
	if (nbr < 0)
	{
		i++;
		nbr *= -1;
	}
	while (nbr > 0)
	{
		nbr = nbr / 10;
		i++;
	}
	return (i);
}
