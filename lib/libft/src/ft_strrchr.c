#include "libft.h"

char	*ft_strrchr(const char *s, int32_t c)
{
	int32_t	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (uint8_t)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
